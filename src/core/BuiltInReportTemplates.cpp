QString text(const char* value)
{
    return QString::fromUtf8(value);
}

QString fieldPlaceholder(const QString& fieldName)
{
    return QStringLiteral("[%1]").arg(fieldName);
}

ReportFrameDefinition standardDataFrame(
    int fieldIndex,
    const QString& fieldName,
    const QRect& bounds,
    int order,
    bool printEntireContents)
{
    ReportFrameDefinition frame;
    frame.signature = LegacyReportFrameSignature;
    frame.sourceId = static_cast<quint16>(std::max(0, fieldIndex));
    frame.order = static_cast<quint16>(std::max(0, order));
    frame.band = StandardFrameBandBody;
    frame.bounds = bounds;
    frame.text = fieldPlaceholder(fieldName);
    frame.fieldPlaceholders = {fieldName};
    frame.kind = ReportFrameKind::Data;
    frame.printEntireContentsFlag = printEntireContents ? 1 : 0;
    return frame;
}

ReportFrameDefinition standardSystemFrame(QString token, const QRect& bounds, int order, int band)
{
    ReportFrameDefinition frame;
    frame.signature = LegacyReportFrameSignature;
    frame.sourceId = StandardFrameSourceIdNone;
    frame.order = static_cast<quint16>(std::max(0, order));
    frame.band = static_cast<quint16>(std::max(0, band));
    frame.bounds = bounds;
    frame.text = QStringLiteral("{%1}").arg(token);
    frame.systemTokens = {std::move(token)};
    frame.kind = ReportFrameKind::SystemText;
    if (band == StandardFrameBandHeader) {
        frame.styleFlags = ReportStyleFlagBold;
    }
    return frame;
}

ReportDefinition standardReportShell(
    const Deck& deck,
    QString name,
    int frameCount,
    int sourceReportIndex)
{
    ReportDefinition report;
    report.name = std::move(name);
    report.formatMagic = QStringLiteral("RPT@#$B");
    report.entrySize = LegacyReportHeaderSize + frameCount * LegacyReportFrameSize;
    report.headerSize = LegacyReportHeaderSize;
    report.declaredFrameCount = frameCount;
    report.formType = ReportFormType::Report;
    report.formWidth = StandardReportPageWidthMils;
    report.formHeight = StandardReportPageHeightMils;
    report.rows = 1;
    report.columns = 1;
    report.marginLeft = 0;
    report.marginTop = 0;
    report.marginRight = 0;
    report.marginBottom = 0;
    report.paperStyleId = StandardReportPaperStyleId;
    report.pageWidth = StandardReportPageWidthMils;
    report.pageHeight = StandardReportPageHeightMils;
    report.orientation = 0;
    applyDefaultReportFonts(deck, &report, sourceReportIndex);
    return report;
}

ReportDefinition standardPageReport(const Deck& deck, QString name)
{
    ReportDefinition report = standardReportShell(deck, std::move(name), deck.fieldCount() + 2, 0);
    int top = StandardReportFieldTopMils;
    for (int fieldIndex = 0; fieldIndex < deck.fieldCount(); ++fieldIndex) {
        const FieldDefinition& field = deck.fieldAt(fieldIndex);
        const int height = field.isNotes() ? StandardReportNotesFieldHeightMils : StandardReportFieldHeightMils;
        report.frames.append(standardDataFrame(
            fieldIndex,
            field.name(),
            QRect(StandardReportFieldLeftMils, top, StandardReportFieldWidthMils, height),
            fieldIndex,
            field.isNotes()));
        top += field.isNotes() ? StandardReportNotesFieldHeightMils : StandardReportFieldVerticalStepMils;
    }
    report.frames.append(standardSystemFrame(
        QStringLiteral("reportname"),
        QRect(StandardReportTitleLeftMils, StandardReportTitleTopMils, StandardReportTitleWidthMils, StandardReportTitleHeightMils),
        deck.fieldCount(),
        StandardFrameBandHeader));
    report.frames.append(standardSystemFrame(
        QStringLiteral("page"),
        QRect(StandardReportPageNumberLeftMils, StandardReportPageNumberTopMils, StandardReportPageNumberWidthMils, StandardReportPageNumberHeightMils),
        deck.fieldCount() + 1,
        StandardFrameBandFooter));
    return report;
}

ReportDefinition standardRowReport(const Deck& deck, QString name)
{
    ReportDefinition report = standardReportShell(deck, std::move(name), deck.fieldCount() + 2, 1);
    for (int fieldIndex = 0; fieldIndex < deck.fieldCount(); ++fieldIndex) {
        const FieldDefinition& field = deck.fieldAt(fieldIndex);
        const int column = fieldIndex % StandardRowReportColumns;
        const int row = fieldIndex / StandardRowReportColumns;
        const int left = StandardRowReportFieldLeftMils + column * StandardRowReportColumnWidthMils;
        const int top = StandardRowReportFieldTopMils + row * StandardRowReportRowHeightMils;
        report.frames.append(standardDataFrame(
            fieldIndex,
            field.name(),
            QRect(left, top, StandardRowReportColumnWidthMils - 120, StandardReportFieldHeightMils),
            fieldIndex,
            field.isNotes()));
    }
    report.frames.append(standardSystemFrame(
        QStringLiteral("reportname"),
        QRect(StandardReportTitleLeftMils, StandardReportTitleTopMils, StandardReportTitleWidthMils, StandardReportTitleHeightMils),
        deck.fieldCount(),
        StandardFrameBandHeader));
    report.frames.append(standardSystemFrame(
        QStringLiteral("page"),
        QRect(StandardReportPageNumberLeftMils, StandardReportPageNumberTopMils, StandardReportPageNumberWidthMils, StandardReportPageNumberHeightMils),
        deck.fieldCount() + 1,
        StandardFrameBandFooter));
    return report;
}

QVector<QString> texts(std::initializer_list<const char*> values)
{
    QVector<QString> result;
    result.reserve(static_cast<int>(values.size()));
    for (const char* value : values) {
        result.append(text(value));
    }
    return result;
}

QRect scaledReportFrameBounds(QRect bounds, int scale)
{
    return QRect(
        bounds.left() * scale,
        bounds.top() * scale,
        bounds.width() * scale,
        bounds.height() * scale);
}

ReportFontDefinition font(const char* faceName, int legacyHeight)
{
    ReportFontDefinition result;
    result.faceName = text(faceName);
    result.legacyHeight = legacyHeight;
    return result;
}

ReportFrameDefinition frame(
    int legacyOffset,
    quint16 signature,
    quint16 sourceId,
    quint16 order,
    quint16 band,
    QRect bounds,
    const char* frameText,
    QVector<QString> fieldPlaceholders,
    QVector<QString> systemTokens,
    ReportFrameKind kind,
    quint8 printEntireContentsFlag,
    quint8 validationFlags,
    quint8 styleFlags,
    int lineBoxShape,
    int lineStyle,
    int fillPattern,
    int cornerRadius)
{
    ReportFrameDefinition result;
    result.legacyOffset = legacyOffset;
    result.signature = signature;
    result.sourceId = sourceId;
    result.order = order;
    result.band = band;
    result.bounds = bounds;
    result.text = text(frameText);
    result.fieldPlaceholders = std::move(fieldPlaceholders);
    result.systemTokens = std::move(systemTokens);
    result.kind = kind;
    result.printEntireContentsFlag = printEntireContentsFlag;
    result.validationFlags = validationFlags;
    result.styleFlags = styleFlags;
    result.lineBoxShape = lineBoxShape;
    result.lineStyle = lineStyle;
    result.fillPattern = fillPattern;
    result.cornerRadius = cornerRadius;
    return result;
}

ReportDefinition report(
    int legacyOffset,
    const char* formatMagic,
    int entrySize,
    int headerSize,
    int declaredFrameCount,
    const char* name,
    ReportFormType formType,
    int formWidth,
    int formHeight,
    int rows,
    int columns,
    ReportFontDefinition dataFont,
    ReportFontDefinition textFont,
    QVector<ReportFrameDefinition> frames)
{
    ReportDefinition result;
    result.legacyOffset = legacyOffset;
    result.formatMagic = text(formatMagic);
    result.entrySize = entrySize;
    result.headerSize = headerSize;
    result.declaredFrameCount = declaredFrameCount;
    result.name = text(name);
    result.formType = formType;
    result.formWidth = formWidth;
    result.formHeight = formHeight;
    result.rows = rows;
    result.columns = columns;
    result.dataFont = std::move(dataFont);
    result.textFont = std::move(textFont);
    for (ReportFrameDefinition& frame : frames) {
        frame.bounds = scaledReportFrameBounds(frame.bounds, BuiltInReportFrameCoordinateScale);
    }
    result.frames = std::move(frames);
    return result;
}

