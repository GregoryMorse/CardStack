const UiMenu* findMenu(int menuId)
{
    const auto it = std::find_if(std::begin(kMenus), std::end(kMenus), [menuId](const UiMenu& menu) {
        return menu.name == menuId;
    });
    return it == std::end(kMenus) ? nullptr : &*it;
}

const UiDialog* findDialog(const char* name)
{
    const auto it = std::find_if(std::begin(kDialogs), std::end(kDialogs), [name](const UiDialog& dialog) {
        return std::strcmp(dialog.name, name) == 0;
    });
    return it == std::end(kDialogs) ? nullptr : &*it;
}

const UiString* findString(int stringId)
{
    const auto it = std::find_if(std::begin(kStrings), std::end(kStrings), [stringId](const UiString& stringValue) {
        return stringValue.id == stringId;
    });
    return it == std::end(kStrings) ? nullptr : &*it;
}

